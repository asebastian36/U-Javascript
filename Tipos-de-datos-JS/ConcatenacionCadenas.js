//  concatenacion de cadenas
var nombre = "Angel";
var apellido = "Franco";

var nombreCompleto = nombre + " " + apellido;
console.log(nombreCompleto);

//  se concatenan
var x = nombreCompleto + 21;
console.log(x);

//  se suma y se concatena
x = nombre + (1 + 1);
console.log(x);

