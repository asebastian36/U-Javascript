/*
    operadores aritmeticos de incremento y decremento
    ++ incremento
    -- decremento
*/

let a, b;
a = 3, b = 2;

//  incremento
//  pre-incremento (el operador antes de la variable)
a = ++a;
console.log(a);
//  post-incremento (el operador despues de la variable)
b = b++;
console.log(b);

let c, d;
c = 5, d = 6;

//  decremento
//  pre-decremento
c = --c;
console.log(c);
//  post-decremento
d = d--;
console.log(d);