//  AND, retorna true o false

//  ejemplo con una evaluacion si un numero esta en el rango asignado
let a = 5;
let valMin = 0, valMax = 15;

//  uso del operador AND
//  ambas expresiones deben cumplirse para retornar true
if (a >= valMin && a <= valMax) console.log("es valido");

else console.log("no es valido");