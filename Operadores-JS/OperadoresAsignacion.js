//  operador de asignacion 
let a = 1;

//  mas tipos de operadores de asignacion:

//   a = a = 1
a += 1;
console.log(a)
//  a = a - 1
a -= 1;
console.log(a)

//  a = a * 2;
a *= 2;
console.log(a)

//  a = a / 1
a /= 1;
console.log(a)

// a = a % 3;
a %= 3;
console.log(a)

//  a = a ** 2
a **= 2;
console.log(a)